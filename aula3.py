'''
DocString
Python = Linguagem de progamação
Tipo de tipagem = Dinâmica / Forte
str = string -> texto
Strings são textos que estão dentro de aspas
(TUDO dentro de aspas é automaticamente uma String, mesmo que seja números)
'''

# Aspas simples
print('sou progamador')
print(1, 'sou "progamador"')


# Aspas duplas
print("sou progamador")
print(2, "sou 'progamador'")


# Escape
print("sou \"progamador\"")


# r
print(r"sou \"progamador\"")